function activateSong(songItem) {
  if (songItem != null && !songItem.classList.contains("active")) {
    const currentSong = document.getElementsByClassName("songItem active")[0];
    const songPlaying = document.getElementsByClassName("modal_header_song")[0];

    currentSong.classList.remove("active");
    songItem.classList.add("active");

    const songIndex = songItem.dataset.index;
    songPlaying.innerText = `♫ ${songList[songIndex].name} ♫`;
    playSong(songIndex);
  }
}

function playSong(songIndex) {
  const song = songList[songIndex];
  player.src = song.path;
  player.load();
  player.play();
}

document.addEventListener("keydown", (event) => {
  const currentPage = document.getElementsByClassName("show")[0];
  const layout = document.getElementById("layout");
  const gacha = document.getElementById("gacha_layout");
  const voucher = document.getElementById("voucher_layout");
  const setting = document.getElementById("setting_layout");

  if (!checkLayout()) {
    if (currentPage.id === "index_page") {
      if (event.key === "Enter") {
        const nextPage = document.getElementById("story_page");
        switchPage(currentPage, nextPage);
      }
    } else if (currentPage.id === "story_page") {
      if (event.key === "ArrowRight") {
        currentIndex = currentIndex === listLength - 1 ? 0 : currentIndex + 1;
        loadPage(currentIndex);
      } else if (event.key === "ArrowLeft") {
        currentIndex = currentIndex === 0 ? listLength - 1 : currentIndex - 1;
        loadPage(currentIndex);
      } else if (event.key === "Enter") {
        const nextPage = document.getElementById("wishes_page");
        switchPage(currentPage, nextPage);
      } else if (event.key === "Escape") {
        const prevPage = document.getElementById("index_page");
        switchPage(currentPage, prevPage);
      }
    } else if (currentPage.id === "wishes_page") {
      if (event.key === "Escape") {
        const prevPage = document.getElementById("story_page");
        if (isFalled) {
          isFalled = false;
          clearInterval(fallingLoop);
        }
        switchPage(currentPage, prevPage);
      } else if (!isFalled) {
        fallingLoop = setInterval(createFallingItems, 300);
        isFalled = true;
      }
    }
  }

  switchLayoutByKey(event, layout, "v", 400);
  switchLayoutByKey(event, gacha, "g", 400);
  switchLayoutByKey(event, setting, "x", 400);
  if (isOpened) switchLayoutByKey(event, voucher, "k", 400);

  if (!isPlaying) {
    isPlaying = true;
    playSong(currentSong);
  }
});

document.addEventListener("click", (event) => {
  const layout = document.getElementById("layout");
  const gacha = document.getElementById("gacha_layout");
  const songItem = event.target.closest(".songItem");
  const voucher = document.getElementById("voucher_layout");
  const setting = document.getElementById("setting_layout");

  switchLayout(event, layout, "layout", 400);
  switchLayout(event, gacha, "gacha_layout", 400);
  switchLayout(event, voucher, "voucher_layout", 400);
  switchLayout(event, setting, "setting_layout", 400);

  activateSong(songItem);

  if (!isPlaying) {
    isPlaying = true;
    playSong(currentSong);
  }
});

document.addEventListener("dblclick", (event) => {
  const gachaItem = document.getElementById("gacha_item");
  const videoEffect = document.getElementById("gacha_effect_vid");

  if (
    event.target.id === "gacha_item" &&
    !gachaItem.classList.contains("startShake")
  ) {
    gachaItem.classList.add("startShake");
    setTimeout(() => {
      videoEffect.src = "../assets/video/explode_effect.mp4";
      videoEffect.classList.add("ExplodeOn");
      videoEffect.load();
      videoEffect.play();
    }, 3000);
    setTimeout(() => {
      videoEffect.pause();
      videoEffect.style.display = "none";
      isOpened = true;
      showPrize();

      localStorage.setItem("hasGachaGift", "true");
    }, 4500);
  }
});

