function loadPage(index) {
  const containerHeader = document.getElementsByClassName("app_header")[0];
  const containerContent = document.getElementsByClassName("app_content")[0];
  const data = listPages[currentIndex];
  const tall = document.getElementsByClassName("tall_image")[0];
  const square = document.getElementsByClassName("square_image")[0];
  const small1 = document.getElementById("small_image1");
  const small2 = document.getElementById("small_image2");
  const wide = document.getElementsByClassName("wide_image")[0];
  const charNote = document.getElementsByClassName("char_note")[0];
  const bannerTitle = document.getElementsByClassName("banner_title")[0];

  containerHeader.style.opacity = 0;
  containerContent.style.opacity = 0;
  containerHeader.style.transform = "translateY(10px)";
  containerContent.style.transform = "translateY(10px)";

  setTimeout(() => {
    tall.src = data.tall;
    square.src = data.square;
    small1.src = data.small1;
    small2.src = data.small2;
    wide.src = data.wide;
    charNote.innerHTML = data.note;
    bannerTitle.innerText = data.title;
    charNote.style.color = data.text_color;
    charNote.style.borderLeftColor = data.border_color;

    containerHeader.style.opacity = 100;
    containerContent.style.opacity = 100;
    containerHeader.style.transform = "translateY(0)";
    containerContent.style.transform = "translateY(0)";
  }, 400);
}

function preloadImages() {
  fallingImages.forEach((element) => {
    const img = new Image();
    img.src = element;
  });

  songList.forEach((element) => {
    const img = new Image();
    img.src = element.image;
  });
}

function createFallingItems() {
  const body = document.body;
  const item = document.createElement("img");
  const randomIndex = Math.floor(Math.random() * fallingLength);
  item.src = fallingImages[randomIndex];
  item.classList.add("falling_items");

  const startX = Math.random() * window.innerWidth;
  const size = Math.random() * 30 + 30;
  const duration = Math.random() * 3 + 2;

  item.style.left = startX + "px";
  item.style.width = size + "px";
  item.style.animationDuration = duration + "s";

  body.append(item);
  setTimeout(() => {
    item.remove();
  }, duration * 1000);
}

function switchPage(currentPage, newPage) {
  currentPage.classList.replace("Intro", "Outro");
  currentPage.classList.replace("show", "hide");
  newPage.classList.replace("hide", "show");
  newPage.classList.replace("Outro", "Intro");
}

